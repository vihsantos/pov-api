class User():
    def __init__(self, _id, nome, usuario, email, dataNascimento, localizacao, foto, senha):
        self._id = _id
        self.nome = nome
        self.usuario = usuario
        self.email = email
        self.dataNascimento = dataNascimento
        self.localizacao = localizacao
        self.foto = foto
        self.senha = senha
